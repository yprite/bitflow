/** Fallback: a filled rectangle for unknown characters. */
export const UNKNOWN_GLYPH: string[] = [
  '11111',
  '10001',
  '10001',
  '10001',
  '10001',
  '10001',
  '11111',
];

export interface GlyphDot {
  /** Column index within the glyph (0–4). */
  col: number;
  /** Row index within the glyph (0–6). */
  row: number;
  /** Whether this dot should be active (rendered). */
  active: boolean;
}

/**
 * Parse a glyph definition into an array of dot positions.
 * Returns ALL positions (active and inactive) so morph logic
 * can interpolate between two glyph states.
 */
export function parseGlyph(char: string): GlyphDot[] {
  const rows = GLYPH_MAP[char.toUpperCase()] ?? GLYPH_MAP[char] ?? UNKNOWN_GLYPH;
  const dots: GlyphDot[] = [];

  for (let row = 0; row < GLYPH_ROWS; row++) {
    const rowStr = rows[row] ?? '00000';
    for (let col = 0; col < GLYPH_COLS; col++) {
      dots.push({
        col,
        row,
        active: rowStr[col] === '1',
      });
    }
  }

  return dots;
}

/**
 * Parse an entire string into positioned dot arrays.
 * Returns dots with absolute x,y positions considering inter-glyph spacing.
 */
export interface TextDot {
  x: number;
  y: number;
  active: boolean;
  charIndex: number;
  glyphCol: number;
  glyphRow: number;
}

export function parseText(
  text: string,
  spacing: number = 1,
): { dots: TextDot[]; width: number; height: number } {
  const dots: TextDot[] = [];
  let cursorX = 0;

  for (let ci = 0; ci < text.length; ci++) {
    const char = text[ci];
    const glyphDots = parseGlyph(char);

    for (const gd of glyphDots) {
      dots.push({
        x: cursorX + gd.col,
        y: gd.row,
        active: gd.active,
        charIndex: ci,
        glyphCol: gd.col,
        glyphRow: gd.row,
      });
    }

    cursorX += GLYPH_COLS + spacing;
  }

  const width = text.length > 0 ? cursorX - spacing : 0;
  const height = GLYPH_ROWS;

  return { dots, width, height };
}
